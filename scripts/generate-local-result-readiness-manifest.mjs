#!/usr/bin/env node

import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { basename, dirname, join } from "node:path";

const fixturesDir = "fixtures/synthetic";
const outPath = "src/lib/localResultReadiness.generated.ts";

const isPlainObject = (value) => Boolean(value && typeof value === "object" && !Array.isArray(value));
const isNonEmptyString = (value) => typeof value === "string" && value.trim().length > 0;
const resultRowsFrom = (value) => {
  if (!isPlainObject(value)) {
    return [];
  }
  const candidates = [value.resultRows, value.findings, value.results, value.report?.resultRows, value.report?.findings];
  return candidates.find(Array.isArray) ?? [];
};
const appendixFrom = (value) => {
  if (!isPlainObject(value)) {
    return null;
  }
  return value.appendix ?? value.report?.appendix ?? null;
};

const rows = readdirSync(fixturesDir)
  .filter((file) => file.endsWith(".result.json"))
  .sort()
  .map((file) => {
    const slug = basename(file, ".result.json");
    const artifact = JSON.parse(readFileSync(join(fixturesDir, file), "utf8"));
    const resultRows = resultRowsFrom(artifact);
    const plainEnglishRows = resultRows.filter((row) => isNonEmptyString(row?.plainEnglishMeaning)).length;
    const probabilities = appendixFrom(artifact)?.probabilities;

    return {
      slug,
      resultRows: resultRows.length,
      plainEnglishRows,
      plainEnglishReady: resultRows.length > 0 && plainEnglishRows === resultRows.length,
      appendixProbabilityCount: Array.isArray(probabilities) ? probabilities.length : 0,
      appendixProbabilityReady: Array.isArray(probabilities) && probabilities.every(isPlainObject),
    };
  });

const source = `// Generated by scripts/generate-local-result-readiness-manifest.mjs.
// Do not edit manually; regenerate after changing fixtures/synthetic/*.result.json.

export type LocalResultReadiness = {
  resultRows: number;
  plainEnglishRows: number;
  plainEnglishReady: boolean;
  appendixProbabilityCount: number;
  appendixProbabilityReady: boolean;
};

export const localResultReadinessBySlug: Record<string, LocalResultReadiness> = ${JSON.stringify(
  Object.fromEntries(rows.map(({ slug, ...row }) => [slug, row])),
  null,
  2,
)};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, source);

console.log(
  JSON.stringify(
    {
      schemaVersion: "soma-reports.local-result-readiness-manifest.v1",
      outPath,
      resultArtifacts: rows.length,
      plainEnglishReady: rows.filter((row) => row.plainEnglishReady).length,
      appendixProbabilityReady: rows.filter((row) => row.appendixProbabilityReady).length,
    },
    null,
    2,
  ),
);
