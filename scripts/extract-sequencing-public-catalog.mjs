#!/usr/bin/env node

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";

const args = new Map();
for (let index = 2; index < process.argv.length; index += 2) {
  args.set(process.argv[index], process.argv[index + 1]);
}

const inputPath = args.get("--input");
const jsonOut = args.get("--json-out");
const tsOut = args.get("--ts-out");

if (!inputPath || !jsonOut || !tsOut) {
  throw new Error(
    "Usage: node scripts/extract-sequencing-public-catalog.mjs --input <html> --json-out <path> --ts-out <path>",
  );
}

const sourceUrl = "https://sequencing.com/apps/app-market/page_view_event/aggregate";
const html = readFileSync(inputPath, "utf8");

const decodeHtml = (value) =>
  value
    .replace(/&amp;/g, "&")
    .replace(/&#039;/g, "'")
    .replace(/&quot;/g, '"')
    .replace(/&nbsp;/g, " ")
    .replace(/&rsquo;/g, "’")
    .replace(/&ldquo;|&rdquo;/g, '"')
    .replace(/&ndash;|&mdash;/g, "-")
    .replace(/<[^>]*>/g, "")
    .replace(/\s+/g, " ")
    .trim();

const toAbsoluteUrl = (url) => (url.startsWith("http") ? url : `https://sequencing.com${url}`);

const slugFromMarketplaceUrl = (url) => {
  const pathname = url.replace(/^https:\/\/sequencing\.com/, "");
  return pathname.split("/").filter(Boolean).pop() ?? "";
};

const extractFirst = (source, pattern) => {
  const match = source.match(pattern);
  return match ? decodeHtml(match[1]) : "";
};

const groupBlocks = html
  .split('<div  class="app-market-gouped-apps-group"')
  .slice(1)
  .map((block) => '<div  class="app-market-gouped-apps-group"' + block);

const byUrl = new Map();

for (const groupBlock of groupBlocks) {
  const category = extractFirst(groupBlock, /app-market-gouped-apps-group-title">([\s\S]*?)<\/h2>/);
  const itemBlocks = groupBlock.split('<div class="app-market-group-item">').slice(1);

  for (const rawItemBlock of itemBlocks) {
    const itemBlock = '<div class="app-market-group-item">' + rawItemBlock;
    const relativeUrl = itemBlock.match(/<a href="([^"]*\/marketplace\/[^"]+)"/)?.[1] ?? "";
    const title = extractFirst(itemBlock, /app-market-group-item-title[\s\S]*?<a href="[^"]+">([\s\S]*?)<\/a>/);

    if (!relativeUrl || !title) {
      continue;
    }

    const marketplaceUrl = toAbsoluteUrl(decodeHtml(relativeUrl));
    const existing = byUrl.get(marketplaceUrl);
    const categoryList = existing?.categories ?? [];
    if (category && !categoryList.includes(category)) {
      categoryList.push(category);
    }

    byUrl.set(marketplaceUrl, {
      title,
      slug: slugFromMarketplaceUrl(marketplaceUrl),
      marketplaceUrl,
      categories: categoryList,
      primaryCategory: categoryList[0] ?? "All Apps and Reports",
      provider:
        existing?.provider ??
        extractFirst(itemBlock, /app-market-group-item-developer">([\s\S]*?)<\/div>/) ??
        "Sequencing.com",
      priceLabel: existing?.priceLabel ?? extractFirst(itemBlock, /app-market-group-item-price[\s\S]*?<a[^>]*>([\s\S]*?)<\/a>/),
      imageAlt: existing?.imageAlt ?? extractFirst(itemBlock, /alt="([^"]*)"/),
      sourceUrl,
      extractionStatus: "direct",
    });
  }
}

const catalog = [...byUrl.values()].sort((a, b) => a.title.localeCompare(b.title));
const generatedAt = new Date().toISOString();
const payload = {
  generatedAt,
  sourceUrl,
  count: catalog.length,
  catalog,
};

mkdirSync(dirname(jsonOut), { recursive: true });
mkdirSync(dirname(tsOut), { recursive: true });
writeFileSync(jsonOut, `${JSON.stringify(payload, null, 2)}\n`);
writeFileSync(
  tsOut,
  [
    "// Generated by scripts/extract-sequencing-public-catalog.mjs.",
    "// Source: https://sequencing.com/apps/app-market/page_view_event/aggregate",
    "",
    "export type MarketplaceCatalogEntry = {",
    "  title: string;",
    "  slug: string;",
    "  marketplaceUrl: string;",
    "  categories: string[];",
    "  primaryCategory: string;",
    "  provider: string;",
    "  priceLabel: string;",
    "  imageAlt: string;",
    "  sourceUrl: string;",
    '  extractionStatus: "direct";',
    "};",
    "",
    `export const publicMarketplaceCatalogGeneratedAt = ${JSON.stringify(generatedAt)};`,
    `export const publicMarketplaceCatalogSourceUrl = ${JSON.stringify(sourceUrl)};`,
    `export const publicMarketplaceCatalogCount = ${catalog.length};`,
    "",
    `export const publicMarketplaceCatalog = ${JSON.stringify(catalog, null, 2)} satisfies MarketplaceCatalogEntry[];`,
    "",
  ].join("\n"),
);

console.log(
  JSON.stringify({
    sourceUrl,
    count: catalog.length,
    jsonOut,
    tsOut,
  }),
);
